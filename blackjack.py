def test_agent(agent, env, num_episodes=10000, record_csv=False):
    """Test agent performance without learning or exploration."""
    total_rewards = []
    game_data = []  # 存储游戏过程数据

    # Temporarily disable exploration for testing
    old_epsilon = agent.epsilon
    agent.epsilon = 0.0  # Pure exploitation

    for episode in range(num_episodes):
        obs, info = env.reset()
        episode_reward = 0
        done = False
        step = 0
        episode_data = []  # 存储当前游戏的每一步数据

        while not done:
            action = agent.get_action(obs)
            next_obs, reward, terminated, truncated, info = env.step(action)

            # 记录当前步骤的数据
            player_sum, dealer_showing, usable_ace = obs
            episode_data.append({
                "episode": episode,
                "step": step,
                "player_sum": player_sum,
                "dealer_showing": dealer_showing,
                "usable_ace": usable_ace,
                "action": "hit" if action == 1 else "stand",
                "reward": reward,

                "terminated": terminated,

            })

            episode_reward += reward
            done = terminated or truncated
            obs = next_obs
            step += 1

        total_rewards.append(episode_reward)
        game_data.extend(episode_data)  # 添加当前游戏的所有步骤

    # Restore original epsilon
    agent.epsilon = old_epsilon

    win_rate = np.mean(np.array(total_rewards) > 0)
    average_reward = np.mean(total_rewards)

    print(f"Test Results over {num_episodes} episodes:")
    print(f"Win Rate: {win_rate:.1%}")
    print(f"Average Reward: {average_reward:.3f}")
    print(f"Standard Deviation: {np.std(total_rewards):.3f}")

    # 保存为CSV文件
    if record_csv and game_data:
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        filename = f"blackjack_test_{timestamp}.csv"

        fieldnames = [
            "episode", "step", "player_sum", "dealer_showing", "usable_ace",
            "action", "reward", "terminated"
        ]

        with open(filename, 'w', newline='') as csvfile:
            writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
            writer.writeheader()
            writer.writerows(game_data)

        print(f"Game data saved to {os.path.abspath(filename)}")

    return win_rate, average_reward


# 测试代理并记录CSV
win_rate, avg_reward = test_agent(agent, env, num_episodes=10000, record_csv=True)